def word_to_bin(word):
    return [f'{ord(i):08b}' for i in word]


if __name__ == '__main__':
    print(word_to_bin('man') == ['01101101', '01100001', '01101110'])
    print(word_to_bin('AB') == ['01000001', '01000010'])
    print(
        word_to_bin('wecking') == ['01110111', '01100101', '01100011', '01101011', '01101001', '01101110', '01100111'])
    print(word_to_bin('Ruby') == ['01010010', '01110101', '01100010', '01111001'])
    print(word_to_bin('Yosemite') ==
          ['01011001', '01101111', '01110011', '01100101', '01101101', '01101001', '01110100', '01100101'])
